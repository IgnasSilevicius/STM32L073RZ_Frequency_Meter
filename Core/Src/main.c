/* USER CODE BEGIN 0 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)
	{
		if (Is_First_Captured==0) // if the first rising edge is not captured
		{
			IC_Val1 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1); // read the first value
			Is_First_Captured = 1;  // set the first captured as true
		}

		else   // If the first rising edge is captured, now we will capture the second edge
		{
			IC_Val2 = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);  // read second value

			if (IC_Val2 > IC_Val1)
			{
				Difference = IC_Val2-IC_Val1;
			}

			else if (IC_Val1 > IC_Val2)
			{
				Difference = (0xffff - IC_Val1) + IC_Val2;
			}

			float refClock = TIMCLOCK/PRESCALERabc;
			
			frequency = refClock/Difference;
			
			// Converting frequency to UART message
			sprintf(txMessage, "Frequency: %.4f Hz\r\n", frequency);
			
			// Converting frequency to I2C message
			sprintf(ToMonitor, "%.2f Hz  ", frequency);
			
			// Adjusting measurement prescaler to have more accurate results

//			if(frequency > 0 && frequency < 300)
//				PRESCALERabc = 128;
//			else if (frequency < 5500)
//				PRESCALERabc = 64;
//			else
//				PRESCALERabc = 16;
//			__HAL_TIM_SET_PRESCALER(htim, PRESCALERabc);
			__HAL_TIM_SET_COUNTER(htim, 0);  // reset the counter
			Is_First_Captured = 0; // set it back to false
		}
	}
}

